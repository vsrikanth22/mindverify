package verify.network.hc.axc;

public class AxcException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1318350656469475636L;

	public AxcException() {
		super();
	}

	public AxcException(String message, Throwable cause) {
		super(message, cause);
	}

	public AxcException(String message) {
		super(message);
	}

	public AxcException(Throwable cause) {
		super(cause);
	}
	
	

}
